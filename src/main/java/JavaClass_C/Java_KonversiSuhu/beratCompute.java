public class beratCompute {
    public static double kilogramToGram(double value){
        return (value*1000);
    }
    
    public static double gramToKilogram(double value){
        return (value/1000);
    }
    
    public static double gramToOns(double value){
        return (value/100);
    }
    
    public static double gramToMiligram(double value){
        return (value*1000);
    }
}
